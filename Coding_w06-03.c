#include <stdio.h>

int main() {

    printf("+------------------------+--------------------------------+\n");
    int x = 12, y = 7, z = 12; 
    printf("|      Expression        |              Result            |\n");
    printf("+------------------------+--------------------------------+\n"); 
    printf("|x>y                     |%32d|\n", x > y); 
    printf("|------------------------|--------------------------------|\n");
    printf("|y<z                     |%32d|\n",y < z);
    printf("|------------------------|--------------------------------|\n");
    printf("|x==z                    |%32d|\n",x == z);
    printf("|------------------------|--------------------------------|\n");
    printf("|x!=y                    |%32d|\n",x != y);
    printf("|------------------------|--------------------------------|\n");
    printf("|!(2*5>=y)||(5!=(5/3))   |%32d|\n",!(2*5 >= y) || (5 != (5/3)));
    printf("|------------------------|--------------------------------|\n");
    printf("|!(x < y)                |%32d|\n",!(x < y));
    printf("|------------------------|--------------------------------|\n");
    printf("|(x+y)>(z*2)             |%32d|\n",(x + y) > (z * 2));
    printf("|------------------------|--------------------------------|\n");
    printf("|(x%%2==0)||(y%%2==1)      |%32d|\n",(x % 2 == 0) || (y % 2 == 1));
    printf("|------------------------|--------------------------------|\n");
    printf("|(x>y)&&(z<y)            |%32d|\n",(x > y) && (z < y));
    printf("+------------------------+--------------------------------+\n");



    return 0;
}