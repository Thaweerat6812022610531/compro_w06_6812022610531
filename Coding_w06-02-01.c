    printf("|i=i+(k*j)  |%7d|%7d|%7d|\n",i, j, k);
    printf("|-----------|-------|-------|-------|\n");
    j = i/2;
    printf("|j=i/2      |%7d|%7d|%7d|\n",i, j, k);
    printf("|-----------|-------|-------|-------|\n");
    k = i % 2;
    printf("|k=i%%2      |%7d|%7d|%7d|\n",i, j, k);
